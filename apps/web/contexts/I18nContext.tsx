interface I18nState {
  lang: Locale;
  setLang: (lang: Locale) => void;
  t: (key: string, vars?: Record<string, string>) => string;
  available: Locale[];
}

const I18nContext = createContext<I18nState | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Locale>("es");

  useEffect(() => {
    const saved = typeof window !== "undefined" ? localStorage.getItem("cl_lang") as Locale | null : null;
    if (saved && dictionaries[saved]) {
      setLang(saved);
    }
  }, []);

  useEffect(() => {
    if (typeof window !== "undefined") {
      localStorage.setItem("cl_lang", lang);
      document.documentElement.lang = lang;
    }
  }, [lang]);

  const t = (key: string, vars?: Record<string, string>) => {
    const value = dictionaries[lang]?.[key] || dictionaries["es"][key] || key;
    if (!vars) return value;
    return Object.keys(vars).reduce((acc, k) => acc.replace(`{${k}}`, vars[k]), value);
  };

  const value = useMemo<I18nState>(() => ({ lang, setLang, t, available: Object.keys(dictionaries) as Locale[] }), [lang]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}
